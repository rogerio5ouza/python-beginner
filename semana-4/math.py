# Funções matemáticas nativa
# As funções min() e max() podem ser usadas para encontrar o valor mais baixo ou mais alto em um iterável:

menor_valor = min(2, 4, 6, 8, 10)
maior_valor = max(2, 4, 6, 8, 10)

print(menor_valor)
print(maior_valor)
